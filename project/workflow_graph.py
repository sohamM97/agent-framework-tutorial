@executor(id="write_proposal")
async def write_proposal(
    details: ProjectDetails, ctx: WorkflowContext[AgentExecutorRequest]
) -> None:
    # We write proposal.md ourselves (deterministic) instead of letting an agent
    # do it — same reasoning as main.py. write_to_file is a @tool but stays
    # directly callable.
    project_path = OUTPUTS_DIR / details.project_name
    proposal_file_path = write_to_file(
        filename="proposal.md", contents=details.proposal, filepath=project_path
    )
    # Stash the dynamic paths in the SHARED workflow state (one State per run) so
    # the review / fix / finalize steps downstream can read them.
    ctx.set_state("project_path", str(project_path))
    ctx.set_state("proposal_file_path", str(proposal_file_path))

    # SOHAM: ctx.send_message is used to send messages to the next
    # agent/executor in the graph.
    await ctx.send_message(
        # SOHAM: AgentExecutorRequest is nothing but a series of messages/conversation
        # that is to be passed to the next agent in the workflow.
        # Source: https://github.com/microsoft/agent-framework/blob/main/python/samples/03-workflows/agents/azure_chat_agents_and_executor.py
        AgentExecutorRequest(
            messages=[
                Message(
                    role="system",
                    contents=[
                        f"Read the contents of this proposal: {proposal_file_path} "
                        f"and code a working solution at {project_path / 'out'} "
                        "directory. IMPORTANT: All project related files (source "
                        "code, readme etc.) should be mandatorily inside the 'out' "
                        "directory. Once you are done coding, inform the user and "
                        "give him the path where your code is."
                    ],
                )
            ],
            should_respond=True,
        )
    )


@executor(id="ask_agent_to_review")
async def ask_agent_to_review(
    _coded: AgentExecutorResponse, ctx: WorkflowContext[AgentExecutorRequest]
) -> None:
    throw_random_exception()
    # XL's reply is irrelevant to Amma — Amma reviews the directory on disk.
    # So we discard the coding response and emit a fresh review request.
    out_dir = Path(ctx.get_state("project_path")) / "out"
    await ctx.send_message(
        AgentExecutorRequest(
            messages=[
                Message(
                    role="system",
                    contents=[f"Review the code under directory: {out_dir}"],
                )
            ],
            should_respond=True,
        )
    )


@executor(id="ask_agent_to_fix")
async def ask_agent_to_fix(
    review: AgentExecutorResponse, ctx: WorkflowContext[AgentExecutorRequest]
) -> None:
    comments = review.agent_response.value.comments
    await ctx.send_message(
        AgentExecutorRequest(
            messages=[
                Message(
                    role="system",
                    contents=[
                        "Code review has flagged the following issues: "
                        + comments
                        + " Fix them. If needed, make sure you read existing code "
                        + "to verify correctness before writing your fixes."
                    ],
                )
            ],
            should_respond=True,
        )
    )


# TODO: Claude Review: this returns True (-> handover -> mini_workflow) on a FAILED
# review too, because `not verdict.success` and `verdict is None` count as "stop".
# That was harmless when the True branch went to the old `done` no-op sink, but now it
# markets an unreviewed product. To route ONLY genuine passes onward, gate handover on
# `verdict is not None and verdict.success and verdict.lgtm`, and send failures
# somewhere else.
def review_passed(review: AgentExecutorResponse) -> bool:
    """Edge condition: stop the XL<->Amma loop when the review is done.

    NOTE — conditions receive exactly what the upstream node sent; here that is
    Amma's AgentExecutorResponse, and `.agent_response.value` is the already-parsed
    CodeReviewOutput (Amma has response_format baked in). Unlike main.py — which
    raised on a missing value and would otherwise spin forever — we treat "no
    value" as a stop, so a flaky review ends the loop instead of looping. The
    framework deliberately does NOT swallow exceptions in conditions (_edge.py), so
    keep this total and side-effect free.
    """
    verdict = review.agent_response.value
    return verdict is None or verdict.lgtm or not verdict.success


class PresentProposal(Executor):
    """Soham announces the proposal and promises the product BEFORE coding starts.

    Claude: positioned between write_proposal and xl so the "you'll have a finished
    product shortly" message lands in the same place main.py emits it (before XL
    codes), NOT after the review. It also forwards XL's coding request downstream.
    Reuses the dev session so the summary has full conversational context.
    """

    def __init__(self, sm_agent, sm_session, id: str = "present_proposal"):
        super().__init__(id=id)
        self._sm_agent = sm_agent
        self._session = sm_session

    @handler
    async def run(
        self,
        xl_request: AgentExecutorRequest,
        ctx: WorkflowContext[AgentExecutorRequest, str],
    ) -> None:
        proposal_file_path = ctx.get_state("proposal_file_path")
        # Claude: stream the summary so it types out live under "Soham" (executor_id
        # "present_proposal"); stream_soham yields one update per chunk.
        await stream_soham(
            self._sm_agent,
            Message(
                role="system",
                contents=[
                    "Summarize the discussion, tell the user that the proposal "
                    f"file is present at {proposal_file_path}, give him the name, "
                    "and tell him he will have a finished product shortly. DO NOT "
                    "ask any questions at this stage, as this is supposed to be the "
                    "final approach."
                ],
            ),
            self._session,
            ctx,
        )
        await ctx.send_message(xl_request)  # -> xl starts coding


@executor(id="handover_to_mini_workflow")
async def handover(review: AgentExecutorResponse, ctx: WorkflowContext[str]):
    # TODO: Claude Review: review_path is None when value is None (a failed review that
    # review_passed still routed here), so the message becomes "Files present under
    # None" and Summarizer reads a bogus path. Fixing the review_passed routing above
    # makes this branch unreachable with a None value.
    review_path = (
        review.agent_response.value.review_path if review.agent_response.value else None
    )
    # Other executors in this project send an AgentExecutorRequest in send_message, this
    # one sends a simple string. This is because AgentExecutorRequest was failing when
    # trying with sequential workflow.
    await ctx.send_message(f"Files present under {review_path}")


# Claude: a stable name so checkpoints can be listed back by workflow_name. Without
# a name, WorkflowBuilder auto-generates one per build, which we'd then have to read
# off the built workflow — a fixed literal keeps list/get_latest calls simple.
WORKFLOW_NAME = "aistudio_dev_workflow"


def build_workflow(checkpoint_storage=None):
    # ONE dev session, shared by the gatherer and the presenter so Soham keeps full
    # conversational memory end to end. XL and Amma get their own isolated sessions
    # (created internally by AgentExecutor).
    sm_session = sm_agent.create_session()
    gather_requirements = GatherRequirements(sm_agent, judge_agent, sm_session)
    present_proposal = PresentProposal(sm_agent, sm_session)
    xl = AgentExecutor(xl_agent, id="xl")
    amma = AgentExecutor(amma_agent, id="amma")
    mini_workflow_exec = WorkflowExecutor(
        mini_workflow,
        id="mini_workflow",
        propagate_request=True,
        allow_direct_output=True,
    )

    # NOTE — max_iterations is the safety cap main.py's `while True` lacks, but it
    # bounds the WHOLE run (every superstep, conversation included), not just the
    # review loop. Default is 100; a very long Q&A could approach it.
    return (
        # Claude: passing checkpoint_storage is what turns checkpointing ON — with it
        # None, every create_checkpoint call is a no-op (_runner.py:214). name lets us
        # list the saved checkpoints back out afterwards.
        WorkflowBuilder(
            start_executor=gather_requirements,
            max_iterations=100,
            name=WORKFLOW_NAME,
            checkpoint_storage=checkpoint_storage,
        )
        .add_edge(gather_requirements, write_proposal)
        # Soham announces the proposal (product-shortly message) BEFORE XL codes.
        .add_edge(write_proposal, present_proposal)
        .add_edge(present_proposal, xl)
        .add_edge(xl, ask_agent_to_review)
        .add_edge(ask_agent_to_review, amma)
        # Switch-case = exactly-one routing (vs. add_edge conditions, where every
        # matching edge fires). Done -> done sink; otherwise -> fix, then loop to XL.
        .add_switch_case_edge_group(
            amma,
            [
                Case(condition=review_passed, target=handover),
                Default(target=ask_agent_to_fix),
            ],
        )
        .add_edge(ask_agent_to_fix, xl)
        .add_edge(handover, mini_workflow_exec)
        .build()
    )


# SOHAM: Earlier, these were in main(). It was moved out just because devui needs
# workflows to be at the module level to be able to detect them.
# Claude: in-memory store — checkpoints live only for this process run. Item 2 of
# the checkpointing TODO (persist to disk/db) would swap this for
# FileCheckpointStorage or a custom CheckpointStorage. Passing it into
# build_workflow is what enables saving.
checkpoint_storage = InMemoryCheckpointStorage()
workflow = build_workflow(checkpoint_storage)


