class GatherRequirements(Executor):
    """Owns the Soham<->user<->Judge loop, then emits a ProjectDetails downstream.

    NOTE — request_info in the GRAPH API does NOT return inline. Calling
    `await ctx.request_info(...)` does not give back the user's answer; it just
    pauses the run. The answer arrives later in a separate @response_handler
    method (matched by request/response types). So instead of one `while` loop,
    the work is split in two: `run` starts it, and the response_handler runs
    once per answer to continue.
    """

    def __init__(self, sm_agent, judge, sm_session, id: str = "gather_requirements"):
        super().__init__(id=id)
        self._sm_agent = sm_agent
        self._judge = judge
        # Claude: ONE source of truth — the shared dev session. Soham AND the judge
        # both run on it, so every user turn lands in the session as a side effect,
        # exactly like main.py (the judge run is what injects the user's message).
        # Accepted tradeoff, same as main.py: the judge's verdicts pollute Soham's
        # history and the judge re-reads its own past verdicts. The proposal and the
        # PresentProposal announce later read this same, complete session.
        self._session = sm_session

    # SOHAM: in an executor, the function args (_trigger in this case) denote
    # the expected input data type, while the WorkflowContext[_, _] data types
    # denote the send_message and yield_output data types respectively. So in
    # this case, the executor sends message of type ProjectDetails to the next
    # executor in the graph, and yields AgentResponseUpdate or str as workflow
    # output. Every (class-based) executor has ONE HANDLER PER INPUT TYPE,
    # and one response handler per (request type - response type).
    # Source:
    # https://learn.microsoft.com/en-us/agent-framework/workflows/executors?pivots=programming-language-python
    @handler
    async def run(
        self,
        _trigger: str,
        # Claude: could be ProjectDetails instead of Never. An executor can have many
        # handlers (here run + on_human_turn); MAF takes each handler's declared
        # send-type and UNIONs them into one set for the whole executor, and that set
        # is what type-checks its outgoing edges. So on_human_turn declaring
        # ProjectDetails already puts it in the set (making the edge to write_proposal
        # valid) whether or not run also names it. But Never is more accurate: run
        # never calls send_message (only request_info + yields), so it sends nothing.
        ctx: WorkflowContext[Never, AgentResponseUpdate | str],
    ) -> None:
        # System messages stay developer-controlled (never interpolate user input):
        # https://learn.microsoft.com/en-us/agent-framework/agents/safety#keep-system-messages-developer-controlled
        # Claude: stream Soham's greeting out as workflow output FIRST (so it types
        # out live), then pause for the user's answer.
        await stream_soham(
            self._sm_agent,
            Message(
                role="system",
                contents=["Greet the user, and ask him his requirements."],
            ),
            self._session,
            ctx,
        )
        # SOHAM: ctx.request_info is used when asking input from the user.
        # The executor must have a @response_handler method to handle user inputs.
        # Source: https://learn.microsoft.com/en-us/agent-framework/workflows/human-in-the-loop?pivots=programming-language-python
        # Any request_info call has two primary args:
        # request_data: The request that goes to whoever is driving the workflow while
        #   asking for a response from them. We can pass our custom data types and data,
        #   and whoever is receiving the request must process them accordingly. Like in
        #   this case, we send a UserPrompt with kind "requirements" or "confirm", and
        #   the response_handler does the processing based on the kind.
        # response_type: The expected response type from the user. In this particular
        #   case, we expect a ProjectRequirements with a project_idea and an optional
        #   additional_remarks. In this case, we process this multi-field response
        #   ourselves in utils.take_input_from_user function. Earlier, this method used
        #   to just take strings as inputs using python's input() function. Now it
        #   handles both strings and custom pydantic data types.

        await ctx.request_info(
            request_data=UserPrompt(kind="requirements"),
            response_type=ProjectRequirements,
        )

    # TODO: Claude Review: dev-ui can't render a ProjectRequirements form for this
    # executor and falls back to a plain string box. Two reasons:
    #   1) The `response` param below is a union (str | ProjectRequirements). dev-ui
    #      builds its input form from this handler's static signature and only accepts
    #      a SINGLE concrete class as the response type (agent_framework_devui
    #      _utils.py:386 -> isinstance(second_param_type, type) is False for a union),
    #      so it returns None and falls back to {"type": "string"} (_executor.py:1125).
    #   2) Deeper: dev-ui keys the form off (executor_id, request_type) only and never
    #      sees the per-call response_type= we pass. Both the requirements turn and the
    #      confirm turn share request_type=UserPrompt (told apart only by `kind` at
    #      runtime), so one request_type can't map to two response shapes.
    # Fix (only if we want dev-ui to RUN this flow, not just the CLI driver): split
    # UserPrompt into distinct request types (e.g. RequirementsPrompt / ConfirmPrompt)
    # and give each its own single-typed response_handler
    # (on_requirements(..., response: ProjectRequirements, ...) and
    # on_confirm(..., response: str, ...)). Also make the follow-up requirements ask
    # (see the request_info at the bottom of this method) use ProjectRequirements too,
    # so every requirements pause renders the same form. The CLI driver
    # (take_input_from_user reads event.response_type per event) already handles the
    # current union fine, so this restructure is purely for dev-ui.
    @response_handler
    async def on_human_turn(
        self,
        # The "run" handler calls request info with request_data: UserPrompt
        # and response_type: ProjectRequirements. And this method
        # "on_human_turn" calls request info with request_data: UserPrompt and
        # response_type: str. This response_handler handles both of these
        # types of request info. Basically, there should be one response
        # handler to handle each type of request info. The good thing is that
        # MAF throws a warning if this is not implemented
        request: UserPrompt,
        response: str | ProjectRequirements,
        ctx: WorkflowContext[ProjectDetails, AgentResponseUpdate | str],
    ) -> None:

        if isinstance(response, ProjectRequirements):
            user_response = f"Project idea: {response.project_idea}"
            if response.additional_remarks:
                user_response += f" Additional remarks: {response.additional_remarks}"
        else:
            user_response = response

        if request.kind == "confirm" and response.strip().lower() == "y":
            # Proposal generated from the shared session, which by now holds the
            # full conversation — the judge ran on this session every prior turn,
            # so every user turn (including the one that flipped `ready`) is already
            # in it. Same prompt as main.py.
            details = await self._sm_agent.run(
                Message(
                    role="system",
                    contents=[
                        "Generate a project name and a proposal based on your "
                        "discussion. The project name should be in snake_case. "
                        "The proposal contents should be in markdown format."
                    ],
                ),
                session=self._session,
                options={"response_format": ProjectDetails},
            )
            await ctx.send_message(details.value)  # -> write_proposal
            return
        # Improvement over main.py (which discarded a non-"y" answer — its TODO
        # asked for exactly this): treat it as more requirements input below.

        # Requirements turn (or a "no, here's more" confirm answer): run the judge
        # ON THE SESSION so `response` is injected into the shared history (this is
        # how main.py gets every user turn into the session), then either ask again
        # or move to confirmation.
        #
        # Claude NOTE — the EARLIER BUGGY version is kept commented just below as a
        # lesson. It ran the judge STATELESS on a separate `self._transcript`, so user
        # turns were never written into the dev session; and on the turn the judge
        # flipped to `ready` we skipped dev.run(response) too, so that message reached
        # neither. Proposal-gen then ran on a session holding only the greeting and
        # "restated the greeting" (see the requirements_intake_assistant output, whose
        # line 1 was literally the greeting). Running the judge ON the session fixes it.
        #     self._transcript.append(Message(role="user", contents=[response]))
        #     verdict = await self._judge.run(self._transcript)  # stateless: no session=
        verdict = await self._judge.run(user_response, session=self._session)
        ready = bool(verdict.value and verdict.value.ready)

        if ready:
            # Claude: this prompt is a fixed string (not an agent reply), so we just
            # yield it as one output event — it still shows up under "Soham" via
            # stream_segment — then pause for the y/n.
            await ctx.yield_output(
                "Looks like we have everything we need. Should we proceed "
                "with the final proposal? (y/n)"
            )
            await ctx.request_info(UserPrompt(kind="confirm"), str)
            return

        # Claude: stream Soham's follow-up question live, then pause for more input.
        await stream_soham(self._sm_agent, user_response, self._session, ctx)
        await ctx.request_info(UserPrompt(kind="requirements"), str)


